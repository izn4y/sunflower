function collapseline() {
  var line = document.getElementById("limit_line");
  if (line.style.display === "none") {
    line.style.display = "block";
  } else {
    line.style.display = "none";
  }
  var line = document.getElementById("limit_line_web");
  if (line.style.display === "none") {
    line.style.display = "block";
  } else {
    line.style.display = "none";
  }
  var line = document.getElementById("limit_line_movie");
  if (line.style.display === "none") {
    line.style.display = "block";
  } else {
    line.style.display = "none";
  }
  var line = document.getElementById("limit_line_software");
  if (line.style.display === "none") {
    line.style.display = "block";
  } else {
    line.style.display = "none";
  }
}
